# ── PT cümle şablonları (per word, minimal fallback) ─────────────────────────
# buildPhase Phase 3, eğer yeterli cümle yoksa sabit fallback kullanır.
# Bu yüzden burada cümle eklemiyoruz — mevcut 'es' cümleleri zaten silinmeyecek.

def make_pt_block(en_words, pt_dict):
    """EN words listesinden PT words listesi üretir."""
    pt_words = []
    for w in en_words:
        wid = w['id']
        if wid not in pt_dict:
            print(f'  ⚠️  Missing PT for: {wid}')
            continue
        pt_word, pt_pron = pt_dict[wid]
        entry = {
            'id':    wid,
            'emoji': w.get('emoji', ''),
            'word':  pt_word,
            'tr':    w.get('tr', ''),
            'pron':  pt_pron,
            'level': w.get('level', 'a1'),
        }
        pt_words.append(entry)
    return pt_words


cats = [
  'animals','colors','numbers','fruits','vegetables','body','family',
  'school','food','greetings','questions','clothing','home','transport',
  'time','jobs','sports','places','adjectives',
]

updated = 0
skipped = 0

for cat in cats:
    path = os.path.join(BASE, f'{cat}-a1.json')
    with open(path, encoding='utf-8-sig') as f:
        d = json.load(f)

    if 'pt' in d.get('translations', {}):
        print(f'SKIP {cat}: pt already exists')
        skipped += 1
        continue

    en_words = d['translations']['en']['words']
    pt_words = make_pt_block(en_words, PT)

    d['translations']['pt'] = {
        'label': 'Português',
        'flag':  '🇧🇷',
        'words': pt_words,
    }

    with open(path, 'w', encoding='utf-8') as f:
        json.dump(d, f, ensure_ascii=False, indent=2)

    print(f'OK {cat}: {len(pt_words)}/{len(en_words)} words added')
    updated += 1

print(f'\nDone: {updated} updated, {skipped} skipped')
